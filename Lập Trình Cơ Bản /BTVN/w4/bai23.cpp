#include <iostream>
using namespace std;

bool snt(int n) {
    if (n < 2) return false;
    for (int i = 2; i < n; i++)
        if (n % i == 0) return false;
    return true;
}

int main() {
    int count = 0, num = 2;
    while (count < 100) {
        if (snt(num)) {
            cout << num << " ";
            count++;
        }
        num++;
    }
    return 0;
}
